# Helper function to get celebrities for a specific date
def get_celebrities_for_date(month: int, day: int) -> list:
    """
    Get list of celebrities for a specific date.
    
    Args:
        month: Month (1-12)
        day: Day (1-31)
    
    Returns:
        List of celebrity dictionaries, or empty list if none found
    """
    return CELEBRITIES_DATABASE.get((month, day), [])

# Helper function to check how many dates are populated
def get_population_stats() -> dict:
    """
    Get statistics about how many dates have celebrities.
    
    Returns:
        Dictionary with stats about populated dates
    """
    total_dates = len(CELEBRITIES_DATABASE)
    populated_dates = sum(1 for celebs in CELEBRITIES_DATABASE.values() if celebs)
    empty_dates = total_dates - populated_dates
    total_celebrities = sum(len(celebs) for celebs in CELEBRITIES_DATABASE.values())
    
    return {
        "total_dates": total_dates,
        "populated_dates": populated_dates,
        "empty_dates": empty_dates,
        "total_celebrities": total_celebrities,
        "percentage_populated": round((populated_dates / total_dates) * 100, 2) if total_dates > 0 else 0
    }

